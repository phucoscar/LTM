    private void btn_sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sendActionPerformed
        // TODO add your handling code here:
        String msg_client = "";
        msg_client = msg_text.getText().trim();
        if (!msg_client.equals("")) {
            MemberController.setMsg_area("[You]:&nbsp&nbsp " + msg_client);
            this.mc.send_client_msg(msg_client);
            msg_text.setText("");
        }
    }//GEN-LAST:event_btn_sendActionPerformed

    private void msg_textKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_msg_textKeyPressed
        // TODO add your handling code here:
        if ((evt.getKeyCode() == KeyEvent.VK_ENTER) && ((evt.getModifiers() & KeyEvent.CTRL_MASK) != 0)) {
            msg_text.setText(msg_text.getText() + '\n');
        }
    }//GEN-LAST:event_msg_textKeyPressed

    private void msg_textKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_msg_textKeyReleased
        // TODO add your handling code here:
        if ((evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) && ((evt.getModifiers() & KeyEvent.CTRL_MASK) == 0)) {
            String msg_client = "";
            msg_client = msg_text.getText().trim();
            if (!msg_client.equals("")) {
                msg_client = msg_client.replaceAll("\n", "<br>");
                MemberController.setMsg_area("[You]:&nbsp&nbsp " + msg_client);
                this.mc.send_client_msg(msg_client);
                msg_text.setText("");
            }
        }
    }//GEN-LAST:event_msg_textKeyReleased

