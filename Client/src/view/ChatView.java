    class CloseWindow extends Thread{
        @Override
        public void run() {
            MemberController.setMsg_area("[SERVER]:&nbsp&nbsp You are trying to exit. Window will be closed.");
            try {
                Thread.sleep(1000);
                mc.member_socket.close();
                cc.client_socket.close();
            }
            catch(Exception e) {
                System.out.println(e);
            }
            close();
            System.exit(0);
        }
    }
    
