    private void btn_kickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kickActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0 || row > tm.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Select a member to kick!");
        }
        else {
            String kicked_nickname = jTable1.getValueAt(row, 0).toString();
            if (row == 0) {
                JOptionPane.showMessageDialog(this, "Can't kick admin!");
            }
            else {
                String msg_client = "Admin wants to kick a member!";
                this.mc.send_client_msg(msg_client);
                this.mc.send_client_msg(kicked_nickname);
                tm.removeRow(row);
            }
        }
    }//GEN-LAST:event_btn_kickActionPerformed

