    public MembersView(MemberController mc, String nickname, int room_id, String str_members) {
        initComponents();
        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        this.mc = mc;
        this.str_members = str_members;
        this.setTitle(nickname);
        idroom_area.setText(""+room_id);
        
        this.initKickButton();
        // Config table and show members
        String[] cols = {"Nickname", "Local address", "Port", "Local port"};
        tm = new DefaultTableModel(cols, 0);
        jTable1.setModel(tm);
        ArrayList<Object[]> list_members = this.get_list_members(str_members);
        for (Object[] member : list_members) {
            tm.addRow(member);
        }
    }
    
    public void initKickButton() {
        if (!this.mc.role.equals("Admin")) {
            btn_kick.setEnabled(false);
        }
    }
    
    public ArrayList<Object[]> get_list_members(String str_members) {
        ArrayList<Object[]> list_members = new ArrayList<>();
        String[] members = str_members.split(";");
        for (String member : members) {
            String[] informartions = member.split("-");
            Object[] o = new Object[]{informartions[0], informartions[1], 
                informartions[2], informartions[3]};
            list_members.add(o);
        }
        return list_members;
    }

